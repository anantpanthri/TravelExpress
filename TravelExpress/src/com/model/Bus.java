package com.model;

public class Bus {
	
	public String service_name;
	public String price_per_mile;

	public String getService_name() {
		return service_name;
	}
	public void setService_name(String service_name) {
		this.service_name = service_name;
	}
	/**
	 * @return the price_per_mile
	 */
	public String getPrice_per_mile() {
		return price_per_mile;
	}
	/**
	 * @param price_per_mile the price_per_mile to set
	 */
	public void setPrice_per_mile(String price_per_mile) {
		this.price_per_mile = price_per_mile;
	}
	
	

}
